#!/usr/bin/env node
// Reads OAuth app credentials from .env and writes server/app-defaults.js
// which gets bundled into the Electron DMG. Run automatically by `npm run dist`.
'use strict';

const fs   = require('fs');
const path = require('path');

require('dotenv').config({ path: path.join(__dirname, '..', '.env') });

const KEYS = [
  'GITHUB_CLIENT_ID', 'GITHUB_CLIENT_SECRET',
  'SLACK_CLIENT_ID',  'SLACK_CLIENT_SECRET',
  'GOOGLE_CLIENT_ID', 'GOOGLE_CLIENT_SECRET',
];

const missing = KEYS.filter(k => !process.env[k]);
if (missing.length) {
  console.warn('[gen-app-defaults] Warning: missing from .env:', missing.join(', '));
  console.warn('  The build will work, but those OAuth flows will need manual setup.');
}

const out = {};
for (const k of KEYS) if (process.env[k]) out[k] = process.env[k];

const content = [
  '// AUTO-GENERATED by scripts/gen-app-defaults.js — do not edit by hand.',
  '// Bundled OAuth app credentials. Gitignored. Re-run `npm run dist` to refresh.',
  'module.exports = ' + JSON.stringify(out, null, 2) + ';',
  '',
].join('\n');

const dest = path.join(__dirname, '..', 'server', 'app-defaults.js');
fs.writeFileSync(dest, content);
console.log('[gen-app-defaults] Written', dest);
